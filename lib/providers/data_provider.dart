  String? _selectedCountryCode;
  bool _isLoading = false;

  List<Country> get countries => _countries;
  List<ServiceCategory> get categories => _categories;
  List<CalculatorItem> get calculatorItems => _calculatorItems;
  String? get selectedCountryCode => _selectedCountryCode;
  bool get isLoading => _isLoading;

  Country? get selectedCountry {
    if (_selectedCountryCode == null) return null;
    try {
      return _countries.firstWhere((c) => c.code == _selectedCountryCode);
    } catch (e) {
      return null;
    }
  }

  double get calculatorTotal {
    return _calculatorItems.fold(0.0, (sum, item) => sum + item.total);
  }

  void selectCountry(String countryCode) {
    _selectedCountryCode = countryCode;
    notifyListeners();
  }

  void addCalculatorItem(CalculatorItem item) {
    _calculatorItems.add(item);
    notifyListeners();
  }

  void updateCalculatorItem(int index, CalculatorItem item) {
    if (index >= 0 && index < _calculatorItems.length) {
      _calculatorItems[index] = item;
      notifyListeners();
    }
  }

  void removeCalculatorItem(int index) {
    if (index >= 0 && index < _calculatorItems.length) {
      _calculatorItems.removeAt(index);
      notifyListeners();
    }
  }

  void clearCalculator() {
    _calculatorItems.clear();
    notifyListeners();
  }

